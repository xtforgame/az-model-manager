import chai from 'chai';
import fs from 'fs';
import { JsonSchemasX } from 'library/manager/azColumnTypes';
import getSchemas from 'library/manager/getTestSchema2';

const { expect } = chai;

describe('JsonSchemasX Investigation with getTestSchema2', () => {
  it('should have pgCollections in campaign schemasMetadata', () => {
    const rawSchemas = getSchemas();
    const jx = new JsonSchemasX('public', rawSchemas);
    jx.toCoreSchemas();

    // Check in schemas
    const campaignSchema = jx.schemas.models.campaign;
    expect(campaignSchema.columns.pgCollections, 'pgCollections must exist in campaign schema').to.exist;
    expect((campaignSchema.columns.pgCollections as any).type[0]).to.equal('hasMany');

    // Check in schemasMetadata
    const campaignMetadata = jx.schemasMetadata.models.campaign;
    const campaignAllMetadata = jx.schemasMetadata.allModels.campaign;

    const campaignCol = jx.schemas.models.campaign.columns.pgCollections as any;
    fs.writeFileSync('schemas.json', JSON.stringify(jx.schemas.models.campaign, null, 2));
    fs.writeFileSync('schemasMetadata.json', JSON.stringify(campaignMetadata, null, 2));
    

    // console.log('Metadata Object Identity:', campaignMetadata === campaignAllMetadata);
    // console.log('Campaign Metadata Columns:', Object.keys(campaignMetadata.columns));

    expect(campaignMetadata, 'campaign metadata in models should exist').to.exist;
    expect(campaignMetadata.columns.pgCollections, 'pgCollections must exist in campaign models metadata').to.exist;
    expect(campaignAllMetadata.columns.pgCollections, 'pgCollections must exist in campaign allModels metadata').to.exist;
  });

  it('should have strict metadata consistency for campaign model', () => {
    const rawSchemas = getSchemas();
    const jx = new JsonSchemasX('public', rawSchemas);
    jx.parseRawSchemas();

    const campaignMetadata = jx.schemasMetadata.models.campaign;

    // 1. Virtual Association Column (parent)
    const parent = campaignMetadata.columns.parent;
    expect(parent, 'parent association should exist').to.exist;
    expect(parent.isVirtual, 'parent should be virtual').to.be.true;
    expect(parent.isAssociationColumn, 'parent should be association column').to.be.true;
    expect(parent.isForeignKey, 'parent should NOT be foreign key').to.be.false;

    // 2. Physical Foreign Key Column (parent_id)
    const parentId = campaignMetadata.columns.parent_id;
    expect(parentId, 'parent_id should exist').to.exist;
    expect(parentId.isVirtual, 'parent_id should NOT be virtual').to.be.false;
    expect(parentId.isAssociationColumn, 'parent_id should NOT be association column').to.be.false;
    expect(parentId.isForeignKey, 'parent_id should be foreign key').to.be.true;
    expect(parentId.ammGeneratedBy, 'parent_id should be generated by parent').to.equal('parent');
    expect(parentId.allowNull, 'parent_id should default to allowNull: true').to.be.true;

    // 3. Virtual Association Column (pgCollections - generated from reverse)
    const pgCollections = campaignMetadata.columns.pgCollections;
    expect(pgCollections, 'pgCollections should exist').to.exist;
    expect(pgCollections.isVirtual, 'pgCollections should be virtual').to.be.true;
    expect(pgCollections.isAssociationColumn, 'pgCollections should be association column').to.be.true;
    expect(pgCollections.isForeignKey, 'pgCollections should NOT be foreign key').to.be.false;

    // 4. Regular Physical Column (id/name)
    const id = campaignMetadata.columns.id;
    expect(id.primaryKey, 'id should be primaryKey').to.be.true;
    expect(id.allowNull, 'primaryKey id should default to allowNull: false').to.be.false;

    const name = campaignMetadata.columns.name;
    expect(name.isVirtual, 'name should NOT be virtual').to.be.false;
    expect(name.isAssociationColumn, 'name should NOT be association column').to.be.false;
    expect(name.isForeignKey, 'name should NOT be foreign key').to.be.false;
    expect(name.allowNull, 'name should default to allowNull: true').to.be.true;

    // Check in raw schemas too
    const rawCampaignName = jx.schemas.models.campaign.columns.name as any;
    expect(rawCampaignName.allowNull, 'raw campaign name should have allowNull: true').to.be.true;

    // Association should NOT have allowNull: true added by this logic
    expect((parent as any).allowNull, 'parent association should NOT have allowNull').to.be.undefined;
  });

  it('should have children in pgCollection schemasMetadata (self-reference)', () => {
    const rawSchemas = getSchemas();
    const jx = new JsonSchemasX('public', rawSchemas);
    jx.parseRawSchemas();

    const pgCollectionMetadata = jx.schemasMetadata.models.pgCollection;
    const children = pgCollectionMetadata.columns.children;
    expect(children, 'children must exist in pgCollection metadata').to.exist;
    expect((children as any).type[0]).to.equal('hasMany');
    
    // Strict checks for self-reference
    expect(children.isVirtual).to.be.true;
    expect(children.isAssociationColumn).to.be.true;
  });

  it('should have through table columns in associationModels metadata', () => {
    const rawSchemas = getSchemas();
    const jx = new JsonSchemasX('public', rawSchemas);
    jx.parseRawSchemas();

    // user.userGroups is belongsToMany through userUserGroup
    const userUserGroupMetadata = jx.schemasMetadata.associationModels.userUserGroup;
    expect(userUserGroupMetadata, 'userUserGroup metadata should exist').to.exist;
    
    // Check for columns added to through table
    const userIdCol = userUserGroupMetadata.columns.user_id;
    expect(userIdCol, 'user_id must exist in userUserGroup metadata').to.exist;
    expect(userIdCol.isForeignKey).to.be.true;
    expect(userIdCol.isVirtual).to.be.false;
    expect(userIdCol.ammGeneratedBy).to.equal('user');

    const groupIdCol = userUserGroupMetadata.columns.group_id;
    expect(groupIdCol, 'group_id must exist in userUserGroup metadata').to.exist;
    expect(groupIdCol.isForeignKey).to.be.true;
    expect(groupIdCol.isVirtual).to.be.false;
  });
});
